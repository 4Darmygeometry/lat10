struct la_dt_insn *gitcapstone_get_from_insn(cs_insn *inputinfo,
    int ir1_num, void *pir1_base)
{
    if (inputinfo == NULL) {
        return NULL;
    }
    struct la_dt_insn *ret;
    if (pir1_base) {
        uint64_t current_address =  (uint64_t)pir1_base +
            (ir1_num * sizeof(struct la_dt_insn));
        ret = (void *)current_address;
    } else {
        ret = malloc(sizeof(struct la_dt_insn));
    }

    //prefix
#ifdef LATX_DISASSEMBLE_TRACE_DEBUG
    strncpy(ret->mnemonic, inputinfo->mnemonic, 32);
    strncpy(ret->op_str, inputinfo->op_str, 160);
#endif
    lsassert(inputinfo->id <= X86_INS_ENDING);
    ret->id = git_x86_insn_tr[inputinfo->id].id;
    for (int i = 0; i < 4; i++) {
        ret->x86.prefix[i] = inputinfo->detail->x86.prefix[i];
    }
    for (int i = 0; i < 4; i++) {
        ret->x86.opcode[i] = inputinfo->detail->x86.opcode[i];
    }
    for (int i = 0; i < 24; i++) {
        ret->bytes[i] = inputinfo->bytes[i];
    }
    ret->x86.addr_size = inputinfo->detail->x86.addr_size;
    ret->x86.rex = inputinfo->detail->x86.rex;
    ret->x86.op_count = inputinfo->detail->x86.op_count;
    for (int i = 0; i < 8; i++) {
        int otype = inputinfo->detail->x86.operands[i].type; 
        ret->x86.operands[i].type = git_x86_insn_op_type[otype].id;
        if (otype == X86_OP_IMM) {
            ret->x86.operands[i].imm =inputinfo->detail->x86.operands[i].imm;
        } else if (otype == X86_OP_REG) {
            lsassert(inputinfo->detail->x86.operands[i].reg <=
                dt_X86_REG_ENDING);
            ret->x86.operands[i].reg =
                git_x86_insn_reg[inputinfo->detail->x86.operands[i].reg].id;
        } else if (otype == X86_OP_MEM) {
            lsassert(inputinfo->detail->x86.operands[i].mem.segment <=
                dt_X86_REG_ENDING);
            lsassert(inputinfo->detail->x86.operands[i].mem.base <=
                dt_X86_REG_ENDING);
            lsassert(inputinfo->detail->x86.operands[i].mem.index <=
                dt_X86_REG_ENDING);
            ret->x86.operands[i].mem.segment =
                git_x86_insn_reg[inputinfo->detail->
                x86.operands[i].mem.segment].id;
            ret->x86.operands[i].mem.default_segment =
                dt_X86_REG_INVALID;
            ret->x86.operands[i].mem.base =
                git_x86_insn_reg[inputinfo->detail->
                x86.operands[i].mem.base].id;
            ret->x86.operands[i].mem.index =
                git_x86_insn_reg[inputinfo->detail->
                x86.operands[i].mem.index].id;
            ret->x86.operands[i].mem.scale =
                inputinfo->detail->x86.operands[i].mem.scale;
            ret->x86.operands[i].mem.disp =
                inputinfo->detail->x86.operands[i].mem.disp;
        }
        ret->x86.operands[i].size = inputinfo->detail->x86.operands[i].size;
        switch (inputinfo->detail->x86.operands[i].access) {
            default:
                break;
            case CS_AC_READ:
                ret->x86.operands[i].access = dt_CS_AC_READ;
                break;
            case CS_AC_WRITE:
                ret->x86.operands[i].access = dt_CS_AC_WRITE;
                break;
            case CS_AC_READ | CS_AC_WRITE:
                ret->x86.operands[i].access = dt_CS_AC_READ|dt_CS_AC_WRITE;
                break;
        }
        ret->x86.operands[i].avx_bcast =
            git_x86_insn_bcast[inputinfo->detail->
            x86.operands[i].avx_bcast].id;
        ret->x86.operands[i].avx_zero_opmask =
            inputinfo->detail->x86.operands[i].avx_zero_opmask;
    }
    ret->x86.avx_cc =
        git_x86_insn_avx_cc[inputinfo->detail->x86.avx_cc].id;
    ret->size = inputinfo->size;
    ret->address = inputinfo->address;
    return ret; 
}

int gitcapstone_get(const uint8_t *code, size_t code_size,
        uint64_t address,
        size_t count, struct la_dt_insn **insn,
        int ir1_num, void *pir1_base)
{
    cs_insn *inputinfo;
    struct la_dt_insn *ret;
    dtassert(git_handle);
    int git_count = latx_cs_disasm(git_handle, code, code_size,
        address, count, &inputinfo, 0, git_cap_tmp);
    if (inputinfo == NULL) {
        *insn = NULL;
        qemu_log_mask(LAT_LOG_DT, "%s: can't disasm code 0x%x\n",
            __func__, *(uint32_t *)code);
        return git_count;
    }
    ret = gitcapstone_get_from_insn(inputinfo, ir1_num, pir1_base);
    *insn = ret;
    return git_count; 
}

